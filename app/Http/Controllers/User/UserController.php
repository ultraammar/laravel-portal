     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        User::insert([
            'name' => $request->name,
            'email' => $request->email,
            'password'=> $request->password,
            'dob' => $request->dob,
            'start_date' => $request->start_date,
            'rent' => $request->rent
        ]);
        return redirect()->route('admin.dashboard')->with('success','User Successfully Added');
    }

